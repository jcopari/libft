#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	ft_strcpy(char *dest, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s)
{
	size_t		len;
	char		*dest;

	len = (ft_strlen(s) + 1);
	dest = malloc(sizeof(char) * len);
	if (dest == NULL)
		return (NULL);
	ft_strcpy(dest, s);
	return ((char *)dest);
}
